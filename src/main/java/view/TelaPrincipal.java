    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        ModeloAgenciaFazendaria remetente = lstAgenciaFazendaria.get(jcbRemetente.getSelectedIndex());

        ModeloAgenciaFazendaria destinatario = lstAgenciaFazendaria.get(jcbDestinatario.getSelectedIndex());

        SParametrosImpressao parametrosImpressao = new SParametrosImpressao();

        Map<String, Object> parametros = new HashMap<>();
        parametros.put("oDestinatario", getRemetenteDestinatario(remetente, "Remetente"));
        parametros.put("oEndereco", getEndereco(remetente));
        parametros.put("oContato", getContato(remetente));

        parametros.put("dDestinatario", getRemetenteDestinatario(destinatario, "Destinatário"));
        parametros.put("dEndereco", getEndereco(destinatario));
        parametros.put("dContato", getContato(destinatario));
        parametros.put("conteudo", stfConteudo.getText());
        
        parametrosImpressao.setPathRelatorio("relatorio/relatorio.jasper");

        parametrosImpressao.setParametros(parametros);
        parametrosImpressao.setMaximizado(true);
        parametrosImpressao.setTitulo("Impressão de Endereços");

        new SImprimirRelatorio().imprimir(parametrosImpressao);
    }//GEN-LAST:event_btnImprimirActionPerformed


    private void atualizarComboBox() {

        this.jcbRemetente.removeAllItems();
        this.jcbDestinatario.removeAllItems();

        this.lstAgenciaFazendaria = new ControleAgenciaFazendaria().pesquisaAgenciaFazendaria("", "sigla_uni");

        for (int i = 0; i < this.lstAgenciaFazendaria.size(); i++) {
            this.jcbRemetente.addItem(this.lstAgenciaFazendaria.get(i).getSigla() + " - " + this.lstAgenciaFazendaria.get(i).getNome());
            this.jcbDestinatario.addItem(this.lstAgenciaFazendaria.get(i).getSigla() + " - " + this.lstAgenciaFazendaria.get(i).getNome());
        }
    }

    private String getRemetenteDestinatario(ModeloAgenciaFazendaria objAgenciaFazendaria, String remetenteDestinatario) {
        String retorno = "<b>" + remetenteDestinatario + ":</b>";

        retorno += "\n" + objAgenciaFazendaria.getSigla() + " - " + objAgenciaFazendaria.getNome();
        retorno += "\n" + objAgenciaFazendaria.getResponsavel();

        return retorno;
    }

    private String getEndereco(ModeloAgenciaFazendaria objAgenciaFazendaria) {
        String retorno = "<b>Endereço:</b>";

        ModeloEndereco objEndereco = objAgenciaFazendaria.getEndereco();

        retorno += "\n" + objEndereco.getLogradouro() + ", " + objEndereco.getNumero() + ", " + objEndereco.getBairro() + ".";
        retorno += "\n" + objEndereco.getMunicipio() + "-" + objEndereco.getUf() + ", " + objEndereco.getCep() + ".";

        if (!objEndereco.getComplemento().isEmpty()) {
            retorno += "\nComplemento: " + objEndereco.getComplemento();
        }

        if (!objEndereco.getPontoReferencia().isEmpty()) {
            retorno += "\nPonto de Referencia: " + objEndereco.getPontoReferencia();
        }

        return retorno;
    }

    private String getContato(ModeloAgenciaFazendaria objAgenciaFazendaria) {
        String retorno = "";

        ModeloContato objContato = objAgenciaFazendaria.getContato();

        if (!objContato.getTelefo().isEmpty()) {
            retorno += "\nTelefone: " + objContato.getTelefo();
        }

        if (!objContato.getFax().isEmpty()) {
            retorno += "\nFax: " + objContato.getFax();
        }

        if (!objContato.getEmail().isEmpty()) {
            retorno += "\nE-mail: " + objContato.getEmail();
        }

        if(!retorno.isEmpty())
            retorno = "<b>Contato:</b>" + retorno;
            
        return retorno;
    }
